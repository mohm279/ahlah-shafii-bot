export const Route = createFileRoute("/api/chat")({
  server: {
    handlers: {
      POST: async ({ request }) => {
        const { messages } = (await request.json()) as {
          messages?: unknown;
        };

        if (!Array.isArray(messages)) {
          return new Response("الرسائل مطلوبة.", {
            status: 400,
          });
        }

        const key = process.env.DEEPSEEK_API_KEY;

        if (!key) {
          return new Response(
            "انتهت صلاحية استخدام المساعد مؤقتًا، يرجى المحاولة لاحقًا.",
            { status: 503 },
          );
        }

        const deepseek = createOpenAICompatible({
          name: "deepseek",
          baseURL: "https://api.deepseek.com",
          apiKey: key,
        });

        const recentMessages = (messages as UIMessage[]).slice(-4);

        const result = streamText({
          model: deepseek("deepseek-v4-flash"),
          system: SYSTEM_PROMPT,
          messages: await convertToModelMessages(recentMessages),
        });

        return result.toUIMessageStreamResponse({
          originalMessages: messages as UIMessage[],

          onError: (error: unknown) => {
            console.error("chat stream error", error);

            const apiError = error as {
              statusCode?: number;
              message?: string;
              responseBody?: string;
            };

            const errorText =
              `${apiError.message ?? ""} ${apiError.responseBody ?? ""}`.toLowerCase();

            if (
              apiError.statusCode === 402 ||
              errorText.includes("insufficient balance")
            ) {
              return "انتهى رصيد استخدام المساعد مؤقتًا، وسيعود للعمل بعد تجديد الرصيد.";
            }

            if (
              apiError.statusCode === 429 ||
              errorText.includes("quota") ||
              errorText.includes("resource_exhausted")
            ) {
              return "تم الوصول إلى حد الاستخدام المسموح حاليًا، يرجى المحاولة لاحقًا.";
            }

            if (apiError.statusCode === 401) {
              return "تعذّر تشغيل المساعد مؤقتًا بسبب مشكلة في الاتصال بالخدمة.";
            }

            return "حدث خطأ مؤقت أثناء الإجابة، يرجى المحاولة مرة أخرى.";
          },
        });
      },
    },
  },
});