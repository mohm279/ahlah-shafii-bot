export const Route = createFileRoute("/api/chat")({
  server: {
    handlers: {
      POST: async ({ request }) => {
        const { messages } = (await request.json()) as { messages?: unknown };

        if (!Array.isArray(messages)) {
          return new Response("Messages are required", { status: 400 });
        }

        const key = process.env.GOOGLE_GENERATIVE_AI_API_KEY;

        if (!key) {
          return new Response("Missing GOOGLE_GENERATIVE_AI_API_KEY", {
            status: 500,
          });
        }

        const result = streamText({
          model: google("gemini-2.5-flash"),
          system: SYSTEM_PROMPT,
          messages: await convertToModelMessages(messages as UIMessage[]),
        });

        return result.toUIMessageStreamResponse({
          originalMessages: messages as UIMessage[],
          onError: (error) => {
            console.error("chat stream error", error);

            const message =
              error instanceof Error ? error.message : "حدث خطأ";

            if (message.includes("429")) {
              return "تم تجاوز الحد المسموح. حاول لاحقاً.";
            }

            if (message.includes("402")) {
              return "نفدت الأرصدة. الرجاء إضافة رصيد.";
            }

            return "حدث خطأ أثناء الإجابة.";
          },
        });
      },
    },
  },
});